class Calculator:

    @staticmethod
    def add(num1, num2):
        return num1 + num2

    @staticmethod
    def sub(num1, num2):
        return num1 - num2
    @staticmethod
    def Root(num1,num2):
        return num1 ** num2
    @staticmethod
    def strength(num1,num2):
        return num1 ** (1/num2)